describe('renderer - location context menu', function() {
  it('copy copies entire input when there is no selection');
  it('copy copies just the selection');
  it('cut cuts entire input when there is no selection');
  it('cut cuts just the selection');
  it('paste replaces entire input when there is no selection');
  it('paste replaces just the selection');
  it('paste and go replaces entire input when there is no selection');
  it('paste and go replaces just the selection');
});
